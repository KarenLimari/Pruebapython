class LargoExcedidoException(Exception):
    """
    Excepción lanzada cuando el nombre de la campaña excede los 250 caracteres.
    """

    def __init__(self, mensaje="El nombre excede los 250 caracteres"):
        """
        Inicializa la excepción con un mensaje predeterminado o personalizado.

        Parámetros:
            mensaje (str): Mensaje de error a mostrar.
        """
        self.mensaje = mensaje
        super().__init__(self.mensaje)


